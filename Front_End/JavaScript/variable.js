// This is a comment in JavaScript. It is ignored by the interpreter.
// Comments are used to explain code and make it more readable.

// Declaring variables using 'var', 'let', and 'const' keywords.

// 'var' is used to declare variables globally or within a function.
var globalVariable = "I'm a global variable";

// 'let' is used to declare block-scoped variables (introduced in ES6).
let blockScopedVariable = "I'm a block-scoped variable";

// 'const' is used to declare block-scoped constants (values that cannot be reassigned).
const constantVariable = "I'm a constant variable";

// Variables can store different types of data, such as strings, numbers, and booleans.

// String variable
let greeting = "Hello, World!";

// Number variable
let age = 25;

// Boolean variable
let isJavaScriptFun = true;

// Variables can also hold more complex data types like arrays and objects.

// Array variable
let fruits = ["apple", "banana", "orange"];

// Object variable
let person = {
  firstName: "John",
  lastName: "Doe",
  age: 30
};

// You can update the value of variables.

greeting = "Hi there!";
age = 26;

// Constants, on the other hand, cannot be reassigned.

// This will result in an error:
// constantVariable = "I can't be changed";

// Variables can be used in expressions and calculations.

let x = 5;
let y = 10;
let sum = x + y;

// Printing variables to the console for debugging.

console.log(globalVariable);
console.log(blockScopedVariable);
console.log(constantVariable);
console.log(greeting);
console.log(age);
console.log(isJavaScriptFun);
console.log(fruits);
console.log(person);
console.log(sum);
